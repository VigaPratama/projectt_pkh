    private void bsaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bsaveActionPerformed
        // TODO add your handling code here:
        String sql = "INSERT INTO laporankegiatan VALUES (?,?,?,?,?,?,?)";
        try {
           PreparedStatement stat = conn.prepareStatement(sql);
           stat.setString(1, id_pendamping.getText());
           stat.setString(2, npendamping.getText());
           stat.setString(3, tlpn.getText());
           stat.setString(4, wkegiatan.getText());
           stat.setString(5, nkegiatan.getText());
           stat.setString(6, id_peserta.getText());
           stat.setString(7, npeserta.getText());
      
           stat.executeUpdate();
           
           JOptionPane.showMessageDialog(null, "Data Berhasil Disimpan");
           kosong();
           id_pendamping.requestFocus();
         
           
        }catch(SQLException e){
            JOptionPane.showMessageDialog(null, "Data gagal Disimpan"+e);
        }

    }//GEN-LAST:event_bsaveActionPerformed

