    private void id_pendampingActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_id_pendampingActionPerformed
        // TODO add your handling code here:
         try {

            String sql = "SELECT * FROM pendampingpkh where id='"
                    + id_pendamping.getText() + "'";
            
            java.sql.Statement stm = conn.createStatement();
            java.sql.ResultSet res = stm.executeQuery(sql);

            while (res.next()) {
                npendamping.setText(res.getString("Nama"));
                tlpn.setText(res.getString("No_HP"));
                
                
            }

        } catch (SQLException e) {
            System.out.println("Error : " + e.getMessage());

        }
    }//GEN-LAST:event_id_pendampingActionPerformed

